    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int sayi =Integer.parseInt(guncelsayi.getText());
        Eleman e = new Eleman(sayi);
        switch(Secenek.getSelectedIndex()){
            case 0:
                liste.basaEkle(e);
                break;
            case 1:
                liste.sonaEkle(e);
                break;    
            case 2:
                int ara=Integer.parseInt(JOptionPane.showInputDialog("Ara giriniz"));
                liste.arayaEkle(e,ara);
                break;
            case 3 :
                liste.listeBasiSil();
                break;
            case 4 :
                liste.listeSonuSil();
                break;
            case 5 :
                int ar=Integer.parseInt(JOptionPane.showInputDialog("Ara giriniz"));
                liste.aradanSil(ar);
                break;
        }
        yeniListe.setText(liste.listele());
    }//GEN-LAST:event_jButton1ActionPerformed

