        int sayi2 =Integer.parseInt(sayi2TF.getText());
        
        Sayi_Liste liste1= Sayi_Liste.sayiEkle(sayi1);
        Sayi_Liste liste2= Sayi_Liste.sayiEkle(sayi2);
        
        Toplam.setText(liste1.topla(liste2).listele());
        
        
        
